int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	flag;

	i = 0;
	if (*str == '\0')
	{
		return (1);
	}
	while (*(str + i) != '\0')
	{
		if ((*(str + i) >= 'a') && (*(str + i) <= 'z'))
		{
			flag = 1;
		}
		else
		{
			return (0);
		}
		i++;
	}
	return (flag);
}
